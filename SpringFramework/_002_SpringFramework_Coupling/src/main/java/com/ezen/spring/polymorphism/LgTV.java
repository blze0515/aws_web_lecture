package com.ezen.spring.polymorphism;

public class LgTV {
	public void turnOn() {
		System.out.println("LgTV --- 전원 켜기");
	}
	
	public void turnOff() {
		System.out.println("LgTV --- 전원 끄기");
	}
	
	public void soundUp() {
		System.out.println("LgTV --- 소리 증가");
	}
	
	public void soundDown() {
		System.out.println("LgTV --- 소리 감소");
	}
}
